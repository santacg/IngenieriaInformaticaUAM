package gui.modelo.exposicion;

/**
 * Clase TipoExpo.
 * Representa los dos posibles tipos de exposición.
 * 
 * Los tipos disponibles son:
 * {@link #TEMPORAL}: La exposición tiene caracter temporal.
 * {@link #PERMANENTE}: La exposición tiene caracter permanente.
 * 
 * @author Carlos García Santa, Joaquín Abad Díaz y Eduardo Junoy Ortega
 */
public enum TipoExpo {
    /** Con fecha de fin definida. */
    TEMPORAL,
    /** Sin fecha de fin definida. */
    PERMANENTE,
}
