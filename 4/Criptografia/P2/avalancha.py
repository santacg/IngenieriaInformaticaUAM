def bit_flip(i, ba):
    flipped = ba.copy()
    flipped[i] = ba[i] ^ 1

    return flipped


def sbox(i, group):
    row = ba2int(group[0:1] + group[-1:])
    col = ba2int(group[1:-1])

    s_box_value = S_BOXES[i][row][col]
    substition = int2ba(s_box_value, length=4)

    return substition


def sbox_test(x):
    group_len = 48 // NUM_S_BOXES

    diffs = [0] * 32 
    for i in range(NUM_S_BOXES):
        original_group = x[i * group_len:group_len * (i + 1)]

        for j in range(group_len):
            bit_flip_group = bit_flip(j, original_group)
            bit_flip_sub = sbox(i, bit_flip_group)

            for k in range(4):
                if bit_flip_sub[k] == 1:
                    diffs[(i * 4) + k] += 1

    diffs = [dif / 6 for dif in diffs]

    return diffs


def avalancha():
    n_test = 10000

    total_diffs = [0] * 32
    for _ in range(n_test):
        A = urandom(48)
        dif = sbox_test(A)
        total_diffs = [a + b for a, b in zip(total_diffs, dif)]

    total_diffs = [t_dif / n_test for t_dif in total_diffs]

    for i, t_dif in enumerate(total_diffs):
        if i % 4 == 0:
            print(f'Caja S{i // 4 + 1}')
        print(f"P(c_{i + 1}=1|~bj) = {t_dif*100:.4f}%")


if __name__ == "__main__":
    avalancha()

